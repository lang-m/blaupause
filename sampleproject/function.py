def add(a, b):
    """Adds two numbers.

    Function to add two numbers.

    Parameters
    ----------
    a : numbers.Real

        First operand.

    b : numbers.Real

        Second operand.

    Returns
    -------
    numbers.Real

        Sum of a and b.

    Examples
    --------

    >>> import sampleproject
    >>> sampleproject.add(1, 2)
    3

    """
    return a + b
